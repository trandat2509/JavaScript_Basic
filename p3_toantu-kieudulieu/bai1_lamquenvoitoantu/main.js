/**
 * Giới thiệu về toán tử trong JS
 * 1. toán tử số học - arithmetic
 * 2. toán tử gán - Assignment
 * 3. Toán tử so sánh - Comparison
 * 4. Toán tử logic - Logical
 */

// VD toán tử số học
var a = 1 + 2;
console.log(a);

// VD toán tử gán
var fullName = "trần hậu nguyên đạt";
console.log(fullName);

// VD toán tử so sánh
var a = 2;
var b = 4;

if (a < b) {
  alert("đúng");
}

// VD toán tử logic: trả về giá trị đúng hoặc sai
var aa = 1;
var bb = 2;

if (aa > 0 || bb > 0) {
  alert("Cả a và b đều lơn hơn 0");
}
